static void convert_to_lcd_coord(int* x, int* y)
{
	*x = *x + LCD_ORIGIN_X;
	*y = *y + LCD_ORIGIN_Y;
}

static void lcd_draw_string(int X0, int Y0, char *text)
{
	DRAW_PrintString(lcdCanvas, X0, Y0, text, COLOR, fontTable);
}

// normalize frequency input to better fit lcd screen
static double normalize_frequency_wave_lcd(double min, double max, double frequency)
{
	const double lcd_max_freq = lcdCanvas->Width/10;
	const double lcd_min_freq = 0.1;

	double lcd_norm_freq = (frequency/(max-min))*lcd_max_freq+lcd_min_freq;

	//printf("Normalized frequency [%f - %d] : %f\n", lcd_min_freq, lcd_max_freq, lcd_norm_freq);

	return lcd_norm_freq;

}

// normalize amplitude input to better fit lcd screen
static double normalize_amplitude_wave_lcd(double min, double max, double amplitude)
{
	const int lcd_max_ampl = (lcdCanvas->Height-LCD_CHAR_HEIGHT)/2;

	double lcd_norm_ampl = (amplitude/(max-min))*lcd_max_ampl;

	//printf("Normalized amplitude [%d - %d] : %f\n", 0, lcd_max_ampl, lcd_norm_ampl);

	return lcd_norm_ampl;

}

static void draw_piano_icon(void)
{
	int white_key_width = 2*LCD_CHAR_WIDTH;
	double black_key_width = (14.0/24.0)*white_key_width; // inspired by commonly dimensions

	//printf("black key width = %d\n", black_key_width);
	DRAW_Filled_Rect(lcdCanvas, LCD_CHAR_WIDTH, LCD_CHAR_HEIGHT+4, lcdCanvas->Width-LCD_CHAR_WIDTH, 24, COLOR);

	// draw white keys
	int i = LCD_CHAR_WIDTH;
	while (i<=(lcdCanvas->Width-LCD_CHAR_WIDTH))
	{
		DRAW_Line(lcdCanvas, i, 24, i, 60, COLOR);
		i = i + white_key_width;
	}
	DRAW_Line(lcdCanvas, LCD_CHAR_WIDTH, 60, (lcdCanvas->Width-LCD_CHAR_WIDTH), 60, COLOR);

	// draw black keys
	double ii = LCD_CHAR_WIDTH + black_key_width;
	int height = 0.75*(60-24)+24;

	//printf("black key height = %d\n", height);

	int k = 0;

	while (k<5)
	{
		DRAW_Filled_Rect(lcdCanvas, ii, 24, ii+black_key_width, height, COLOR);
		if (k==2)
			ii = ii + 3*black_key_width;
		else
			ii = ii + 2*black_key_width;

		k++;
	}

}

